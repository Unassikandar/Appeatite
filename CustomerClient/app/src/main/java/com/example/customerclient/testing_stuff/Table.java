package com.example.customerclient.testing_stuff;

public class Table {

    private String name;
    private String number;
    private String restaurantID;

    Table(){
        name = "";
        number = "";
        restaurantID = "";
    }

    public String getName() {
        return name;
    }

    public String getNumber() {
        return number;
    }

    public String getRestaurantID() {
        return restaurantID;
    }
}
